WHILE @i<=30
BEGIN
    INSERT INTO KhachThue
    VALUES(
      CONCAT('KH',RIGHT('000'+CAST(@i AS VARCHAR),3)),
      N'Khách thuê '+CAST(@i AS NVARCHAR),
      RIGHT('000000000000'+CAST(100000000+@i AS VARCHAR),12),
      DATEADD(YEAR,-20-(@i%10),GETDATE()),
      CONCAT('090',FORMAT(@i,'0000000')),
      N'Hà Nội',
      CONCAT('kh',@i,'@gmail.com')
    );
    SET @i=@i+1;
