INSERT INTO Phong
SELECT
    CONCAT('P',RIGHT('000'+CAST(v.n AS VARCHAR),3)),
    CASE
        WHEN v.n<=7 THEN 'KT001'
        WHEN v.n<=14 THEN 'KT002'
        ELSE 'KT003'
    END,
    N'Phòng ' + CAST(v.n AS NVARCHAR),
    CASE WHEN v.n%4=0 THEN N'VIP' ELSE N'Standard' END,
    18 + v.n,
    2500000 + (v.n*100000),
    CASE WHEN v.n%3=0 THEN N'Trống' ELSE N'Đang thuê' END
FROM (
VALUES
(111),(112),(300),(400),(500),(600),(700),(800),(900),(1234),
(101),(102),(103),(104),(105),(106),(107),(108),(109),(2222)
) v(n);
